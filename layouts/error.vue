<template>
  <Banner no-parallax>
    <div class="text-center">
      <h1 class="text-white text-smooth text-shadow">
        <b>{{ error.statusCode }}</b>
      </h1>
      <h4 class="text-white text-smooth text-shadow">
        <b>
          {{ is404 ? "Oops! The page you are looking can't be found.”" : 'Something went wrong.”' }} 🙁
        </b>
      </h4>
      <Button v-if="is404" tag="a" type="default" class="mt-3" href="/">
        Go Back Home
      </Button>
    </div>
  </Banner>
</template>

<script>
import Banner from '~/components/Base/Banner'
import Button from '~/components/Argon/Button'

export default {
  components: { Banner, Button },
  props: {
    error: {
      type: Object,
      default: () => ({})
    }
  },
  computed: {
    is404() {
      return this.error.statusCode === 404
    }
  },
  head: () => ({
    title: `Error page | ${process.env.AUTHOR}`
  })
}
</script>
